let currentLang = localStorage.getItem('siteLang') || 'ar';
const btn = document.getElementById('langToggle');
function applyLang(lang){
  currentLang = lang;
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  document.body.dir = document.documentElement.dir;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key]) el.textContent = translations[lang][key];
  });
  btn.textContent = lang === 'ar' ? 'EN' : 'AR';
  document.querySelectorAll('[data-gallery]').forEach(item => {
    const index = Number(item.getAttribute('data-gallery'));
    const caption = galleryCaptions[lang][index];
    if (caption) {
      item.dataset.caption = caption;
      item.setAttribute('aria-label', caption);
      const img = item.querySelector('img');
      const label = item.querySelector('span');
      if (img) img.alt = caption;
      if (label) label.textContent = caption;
    }
  });
  document.querySelectorAll('[data-marquee]').forEach(item => {
    const key = item.getAttribute('data-marquee');
    if (marqueeText[lang][key]) item.textContent = marqueeText[lang][key];
  });
  document.querySelectorAll('[data-platform]').forEach(item => {
    const key = item.getAttribute('data-platform');
    const handle = item.querySelector('span') ? item.querySelector('span').outerHTML : '';
    if (platformText[lang][key]) item.innerHTML = platformText[lang][key] + ' ' + handle;
  });
  localStorage.setItem('siteLang', lang);
}
btn.addEventListener('click', () => applyLang(currentLang === 'ar' ? 'en' : 'ar'));
applyLang(currentLang);

document.getElementById('year').textContent = new Date().getFullYear();

const io = new IntersectionObserver((entries)=>{
  entries.forEach(entry=>{ if(entry.isIntersecting){ entry.target.classList.add('visible'); io.unobserve(entry.target); } });
},{threshold:.12});
document.querySelectorAll('.reveal').forEach(el=>io.observe(el));

const lightbox = document.getElementById('lightbox');
const lightboxImg = lightbox.querySelector('img');
const lightboxText = lightbox.querySelector('p');
document.querySelectorAll('.gallery-item').forEach(item => {
  item.addEventListener('click', e => {
    e.preventDefault();
    lightboxImg.src = item.href;
    lightboxText.textContent = item.dataset.caption || '';
    lightbox.classList.add('active');
  });
});
function closeLightbox(){ lightbox.classList.remove('active'); lightboxImg.src=''; }
lightbox.addEventListener('click', e => { if(e.target === lightbox) closeLightbox(); });
document.querySelector('.lightbox-close').addEventListener('click', closeLightbox);
document.addEventListener('keydown', e => { if(e.key === 'Escape') closeLightbox(); });
